package in.co.rays.basic;

public class Addition {

	public static void main(String[] args) {

		int a = 5;
		int b = 4;
		int c = 2;

		int total = a + b * c;
		int value = a - b * c;
		int equal = a / b * c;

		System.out.println("Total is " + total + "." + "\n");
		System.out.println("Value is " + value + "." + "\n");
		System.out.println("Equal is " + equal + "." + "\n");

	}
}
