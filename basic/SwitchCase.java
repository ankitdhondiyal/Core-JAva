package in.co.rays.basic;

public class SwitchCase {
	public static void main(String[] args) {
		
		 int A=10;
		 
		 switch (A) {
		 
		 case 10:
			 System.out.println("Case 1 is satisfied ");
			 
			 break;
			 
		 case 20:
			 System.out.println("Case 2 is satisfied");

			 break;
			 
		 case 30:
			 System.out.println("Case 3 is satisfied");
			 
			 break;
			 
			 default: 
				 System.out.println("This is default");
				 
				 break;
		 }
		 
       	}
			 
		 }